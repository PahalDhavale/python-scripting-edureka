import math 
print(math.fsum(range(10)))   
print(sum(range(10))) 
arr = [1, 4, 6] 
print(math.fsum(arr))
print(sum(arr))

# Floating point list 
arr = [2.5, 2.4, 3.09] 
print(math.fsum(arr))
print(sum(arr))


